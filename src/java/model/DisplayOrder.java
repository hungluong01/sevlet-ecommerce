package model;

/**
 *
 * @author ToanPham
 */
public class DisplayOrder {
    private String img, name, size;
    private float price;
    private int quantity;

    public DisplayOrder() {
    }

    public DisplayOrder(String img, String name, String size, float price, int quantity) {
        this.img = img;
        this.name = name;
        this.size = size;
        this.price = price;
        this.quantity = quantity;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
    
}
